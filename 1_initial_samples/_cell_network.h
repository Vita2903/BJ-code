int make_cell(int poly_class, int e1, int e2, int e3, int e4, int e5, int e6, int e7, int e8, int e9, int e10, int e11, int e12){
    
    int c_id=c_freeId.get(Nc+1);
    edges[c_id][1]=c_id;
    edges[c_id][2]=poly_class;
    edges[c_id][3]=e1;
    edges[c_id][4]=e2;
    edges[c_id][5]=e3;
    edges[c_id][6]=e4;
    edges[c_id][7]=e5;
    edges[c_id][8]=e6;
    edges[c_id][9]=e7;
    edges[c_id][10]=e8;
    edges[c_id][11]=e9;
    edges[c_id][12]=e10;
    edges[c_id][13]=e11;
    edges[c_id][14]=e12;
    
    //ATTRIBUTES
    c_area[c_id]=0;
    
    if(c_id>Nc) Nc++;
        
    return c_id;
}
