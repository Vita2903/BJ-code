int getID(int vec){
    if(ids[vec][0]==0) {
        if(vec==1)        { Nv++; return Nv; }
        else if(vec==2)   { Ne++; return Ne; }
        else if(vec==3)   { Nc++; return Nc; }
    }
    ids[vec][0]--; return ids[vec][ids[vec][0]+1];
}
//****************************************************************************
void freeID(int vec, int i){
    ids[vec][0]++; ids[vec][ids[vec][0]]=i;
}
